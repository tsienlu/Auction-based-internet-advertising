    private void createEnterpriseJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEnterpriseJBtnActionPerformed
        // TODO add your handling code here:
        Network network = (Network) networkJCombo.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseJCombo.getSelectedItem();
        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "InvalidInput");
            return;
        }
        String name = nameJtextField.getText();
        for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (e.getName().equals(name)) {
                JOptionPane.showMessageDialog(null, "Enterprise already exists");
                return;
            }
        }
        Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, type);
        populateEnterpriseTable();
    }//GEN-LAST:event_createEnterpriseJBtnActionPerformed

