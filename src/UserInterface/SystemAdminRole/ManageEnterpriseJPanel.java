    private void populateEnterpriseTable() {
        DefaultTableModel dtm = (DefaultTableModel) enterpriseJTable.getModel();
        dtm.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
                Object row[] = new Object[3];
                row[0] = e;
                row[1] = network.getName();
                row[2] = e.getEnterpriseType();
                dtm.addRow(row);
            }
        }
    }

    private void populateComboBox() {
        networkJCombo.removeAllItems();
        enterpriseJCombo.removeAllItems();

        for (Network n : system.getNetworkList()) {
            networkJCombo.addItem(n);
        }
        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            enterpriseJCombo.addItem(type);
        }
    }
