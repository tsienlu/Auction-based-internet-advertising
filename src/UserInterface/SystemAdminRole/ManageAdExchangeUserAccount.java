    private void populateAdExchangeCombo() {
        adExchangeComboBox.removeAllItems();
        for (AdExchange ae : system.getAdExchangeList().getAdExchangeList()) {
            adExchangeComboBox.addItem(ae);
        }
    }

    private void populateAdExchangeTable(AdExchange ae) {
        DefaultTableModel dtm = (DefaultTableModel) adExchangeTable.getModel();
        dtm.setRowCount(0);

        for (UserAccount ua : adExchange.getUserAccountDirectory().getUserAccountList()) {

            Object row[] = new Object[2];
            row[0] = ua;
            row[1] = ae;
            dtm.addRow(row);
        }
    }

