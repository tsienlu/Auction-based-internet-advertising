    private void createJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createJButtonActionPerformed
        // TODO add your handling code here:
        if (usernameJTextField.getText().length() > 0 && passwordJTextField.getText().length() > 0 && nameJTextField.getText().length() > 0) {
            String username = usernameJTextField.getText();
            String password = passwordJTextField.getText();
            
            UserAccount userAccount=adExchange.getUserAccountDirectory().searchuser(username);
            if(userAccount==null) {
            
            Employee e = new Employee();

            String name = nameJTextField.getText();

            e.setName(name);
            adExchange.getUserAccountDirectory().createUserAccount(username, password, e, new AdExAdminRole());

            populateAdExchangeTable(adExchange);
        }else{
                if(userAccount.getUsername().equals(username)){
                    JOptionPane.showMessageDialog(null,"Already exists");
                }
            }
        }
            else {
            JOptionPane.showMessageDialog(null, "Fields are null");
        }
        
    }//GEN-LAST:event_createJButtonActionPerformed


