    private void populateRequestTable() {
        DefaultTableModel dtm = (DefaultTableModel) financeRequestJTable.getModel();
        dtm.setRowCount(0);
        for (Network n : system.getNetworkList()) {
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {

                for (WorkRequest request : e.getWorkQueue().getWorkRequestList()) {
                    Object row[] = new Object[4];
                    row[0] = request.getSender();
                    row[1] = request.getReceiver();
                    if (e instanceof AdvertiserEnterprise) {
                        AdvertiserEnterprise ae = (AdvertiserEnterprise) e;
                        for (AdvertiserOrder ao : ae.getMasterAdvertiserOrderCatalog().getMasterOrderList()) {
                            row[2] = ao.getOrderAmount();
                        }

                    }
                    String result = ((PublisherFinanceWorkRequest) request).getRequestStatus();
                    row[3] = result == null ? "Waiting" : result;
                    dtm.addRow(row);
                }
            }
        }
    }

