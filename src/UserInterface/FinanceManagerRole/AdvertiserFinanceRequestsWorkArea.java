    private void createRequestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createRequestJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = financeRequestJTable.getSelectedRow();
        if (selectedRow <= 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        PublisherFinanceWorkRequest request = (PublisherFinanceWorkRequest) financeRequestJTable.getValueAt(selectedRow, 0);

        String message = messageJTextField.getText();

        request.setSender(ua);
        request.setStatus("Sent");
        request.setMessage(message);

        Enterprise ent = null;
        for (Network n : system.getNetworkList()) {
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
                if (e instanceof PublisherEnterprise) {
                    ent = enterprise;
                }
            }

            if (ent != null) {
                ent.getWorkQueue().getWorkRequestList().add(request);
                ua.getWorkQueue().getWorkRequestList().add(request);
            }
        }
        populateRequestTable();
    }//GEN-LAST:event_createRequestJButtonActionPerformed

